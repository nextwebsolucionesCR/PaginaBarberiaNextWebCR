/* I18N LOGIC */
const langToggleBtn = document.getElementById('lang-toggle');
const langTextSpan = langToggleBtn ? langToggleBtn.querySelector('.lang-text') : null;

// Available languages in logic order
const availableLangs = ['es', 'en', 'pt', 'fr', 'zh'];
const DEFAULT_LANG = 'es';

// Check saved logic
let currentLang = localStorage.getItem('language') || DEFAULT_LANG;
if (!translations[currentLang]) currentLang = DEFAULT_LANG;

function getText(lang, key) {
    if (translations[lang] && translations[lang][key] !== undefined) return translations[lang][key];
    if (translations[DEFAULT_LANG] && translations[DEFAULT_LANG][key] !== undefined) return translations[DEFAULT_LANG][key];
    return key; // Return key if missing to debug
}

function updateLanguage(lang) {
    if (!translations[lang]) lang = DEFAULT_LANG;
    console.log("Switching to language:", lang);

    // Update Text Content
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        const value = getText(lang, key);

        if (value !== undefined && value !== null) {
            // Preserve Icons if present in first child
            const icon = element.querySelector('i');
            if (icon && element.childNodes.length > 1) {
                // Text likely follows icon
                // We need to be careful not to nuke the icon
                // Simple approach: Rebuild innerHTML
                element.innerHTML = icon.outerHTML + ' ' + value;
            } else {
                // Replace content (safe for simple text)
                // Check if it's a marquee with inner spans? No, usually marquee spans have their own data-i18n
                element.innerHTML = value;
            }
        }
    });

    // Update Placeholders
    const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    placeholders.forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        const value = getText(lang, key);
        if (value) element.placeholder = value;
    });

    // Update Flag/Text on Button
    const btn = document.querySelector('.nav-lang');
    if (btn) {
        btn.innerText = lang.toUpperCase();
    }

    // Save and Update State
    localStorage.setItem('language', lang);
    currentLang = lang;
    document.documentElement.lang = lang; // Accessibility
}

// Global Toggle Function
function toggleLanguage() {
    const idx = availableLangs.indexOf(currentLang);
    const nextLang = availableLangs[(idx + 1) % availableLangs.length];
    updateLanguage(nextLang);
}

// Bind to button
document.addEventListener('DOMContentLoaded', () => {
    const btn = document.querySelector('.nav-lang');
    if (btn) {
        btn.addEventListener('click', toggleLanguage);
    }
    // Init Loading
    updateLanguage(currentLang);
});
